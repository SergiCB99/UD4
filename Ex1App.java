
public class Ex1App {

	public static void main(String[] args) {
		
		//Declaro dos variables numericas
		int a= 10;
		int b = 5;
		
		//Declaro la variable en la que guardare los resultados
		double resultado;
		
		//Hago las operaciones y muestro el resultado
		resultado=a+b;
		System.out.println("Suma "+resultado);
		
		resultado=a-b;
		System.out.println("Resta "+resultado);
		
		resultado=a*b;
		System.out.println("Multiplicacion "+resultado);
		
		resultado=a/b;
		System.out.println("Division "+resultado);
		
		resultado=a%b;
		System.out.println("Modulo "+resultado);
	}
	
}
